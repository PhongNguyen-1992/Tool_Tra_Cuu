        function selectPackage(packageId) {
            // Remove active class from all packages
            document.querySelectorAll('.package-item').forEach(item => {
                item.classList.remove('active');
            });
            
            // Add active class to selected package
            event.target.closest('.package-item').classList.add('active');
            
            // Display package details
            displayPackageDetails(packageId);
        }

        function displayPackageDetails(packageId) {
            const package = packageData[packageId];
            const detailsContainer = document.getElementById('package-details');
            
            let servicesHtml = '';
            Object.keys(package.services).forEach(serviceType => {
                const service = package.services[serviceType];
                let priceRowsHtml = '';
                
                Object.keys(service).forEach(region => {
                    if (region !== 'highlighted') {
                        const isHighlighted = service.highlighted && serviceType === 'Combo Giải trí';
                        priceRowsHtml += `
                            <div class="price-row">
                                <span class="price-label">${region}:</span>
                                <span class="price-value ${isHighlighted ? 'highlighted' : ''}">${service[region]}đ</span>
                            </div>
                        `;
                    }
                });
                
                servicesHtml += `
                    <div class="service-card">
                        <div class="service-type">${serviceType}</div>
                        <div class="price-table">
                            ${priceRowsHtml}
                        </div>
                        <div class="equipment-info">
                            <strong>📦 Thiết bị:</strong> ${package.equipment}<br>
                            <strong>💰 PMH:</strong> <div style="margin: 0 0 0 50px">${package.pmh}</div>
                        </div>
                    </div>
                `;
            });
            
            detailsContainer.innerHTML = `
                <div class="details-title">📊 ${package.name} - ${package.speed}</div>
                <div class="services-grid">
                    ${servicesHtml}
                </div>
                <div class="notes">
                    <h3>📝 Lưu ý:</h3>
                    <p>${package.note}</p>
                </div>
            `;
        }
  